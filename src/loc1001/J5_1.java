package loc1001;

public class J5_1 {
    public static void main(String[] args) {
        for(int i=1;i<=5;i++) {
            for(int j=0;j<i;j++) {
                System.out.print("*");
            }
        System.out.println();
        }
        for(int i=4; i>0;i--) {
            for(int s=1;s<=i;s++) {
                System.out.print("*");
            }
        System.out.println();
        }
    }
}
